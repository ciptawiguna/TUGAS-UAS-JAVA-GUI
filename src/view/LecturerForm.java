    private void jButtonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSimpanActionPerformed
        // TODO add your handling code here:
         String nama     = jTextFieldname.getText().trim();
        String keahlian = jTextFieldexpertise.getText().trim();
        String nidn     = jTextFieldnidn.getText().trim();
        String nik      = jTextFieldcardID.getText().trim();
 
        if (nama.isEmpty() || keahlian.isEmpty() || nidn.isEmpty() || nik.isEmpty()) {
            JOptionPane.showMessageDialog(this, "Semua kolom harus diisi!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
 
        try {
            Lecturer lecturer = new Lecturer(nik, nama, nidn, keahlian);
            lecturerController.saveOrUpdate(lecturer, selectedNidn);
            showInfo("Data Dosen berhasil disimpan!");
            clearForm();
            loadLecturerData();
        } catch (IllegalArgumentException ex) {
            showError(ex.getMessage());
        } catch (SQLException ex) {
            showError("Gagal menyimpan: " + ex.getMessage());
        }
    }//GEN-LAST:event_jButtonSimpanActionPerformed

