    private void jTableDataDosenMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableDataDosenMouseClicked
        // TODO add your handling code here:
        int row = jTableDataDosen.getSelectedRow();
        if (row >= 0) {
            selectedNidn = jTableDataDosen.getValueAt(row, COL_NIDN).toString();
            jTextFieldnidn.setText(jTableDataDosen.getValueAt(row, COL_NIDN).toString());
            jTextFieldcardID.setText(jTableDataDosen.getValueAt(row, COL_NIK).toString());
            jTextFieldname.setText(jTableDataDosen.getValueAt(row, COL_NAMA).toString());
            jTextFieldexpertise.setText(jTableDataDosen.getValueAt(row, COL_KEAHLIAN).toString());
            jTextFieldnidn.setEditable(false);
        }
    }//GEN-LAST:event_jTableDataDosenMouseClicked

