    private void jTableDataMKMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_jTableDataMKMouseClicked
        // TODO add your handling code here:
        int row = jTableDataMK.getSelectedRow();
        if (row >= 0) {
            selectedCode = jTableDataMK.getValueAt(row, COL_KODE).toString();
            jTextFieldKode.setText(jTableDataMK.getValueAt(row, COL_KODE).toString());
            jTextFieldNama.setText(jTableDataMK.getValueAt(row, COL_NAMA).toString());
            jTextFieldSKS.setText(jTableDataMK.getValueAt(row, COL_SKS).toString());
            jTextFieldSemester.setText(jTableDataMK.getValueAt(row, COL_SEMESTER).toString());
            jTextFieldKode.setEditable(false);
        }
    }//GEN-LAST:event_jTableDataMKMouseClicked

