    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        // TODO add your handling code here:
        if (selectedCode == null) {
            JOptionPane.showMessageDialog(this, "Pilih data di tabel dulu!", "Peringatan", JOptionPane.WARNING_MESSAGE);
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(
            this, "Yakin hapus MK: " + selectedCode + "?",
            "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION
        );
        if (confirm == JOptionPane.YES_OPTION) {
            try {
                courseController.delete(selectedCode);
                showInfo("Mata Kuliah berhasil dihapus!");
                clearForm();
                loadCourseData();
            } catch (SQLException ex) {
                showError("Gagal menghapus: " + ex.getMessage());
            }
        }
    }//GEN-LAST:event_jButtonDeleteActionPerformed

