    private void jButtonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSimpanActionPerformed
        // TODO add your handling code here:
    try {
            String kode     = jTextFieldKode.getText();
            String nama     = jTextFieldNama.getText();
            int    sks      = Integer.parseInt(jTextFieldSKS.getText().trim());
            int    semester = Integer.parseInt(jTextFieldSemester.getText().trim());
 
            Course course = new Course(kode, nama, sks, semester);
            courseController.saveOrUpdate(course, selectedCode);
 
            showInfo("Data Mata Kuliah berhasil disimpan!");
            clearForm();
            loadCourseData();
        } catch (NumberFormatException e) {
            showError("SKS dan Semester harus berupa angka!");
        } catch (IllegalArgumentException e) {
            showError(e.getMessage());
        } catch (SQLException e) {
            showError("Gagal menyimpan: " + e.getMessage());
        }
    }//GEN-LAST:event_jButtonSimpanActionPerformed

