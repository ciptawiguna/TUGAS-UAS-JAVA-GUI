    private void loadCourseData() {
        try {
            allData     = courseController.getAll();
            currentPage = 1;
            displayData();
        } catch (SQLException e) {
            showError("Gagal memuat data: " + e.getMessage());
        }
    }
    /**
     * Menampilkan data ke tabel sesuai keyword pencarian dan halaman aktif.
     * Jika ada keyword, seluruh hasil ditampilkan tanpa pagination.
     * Jika kosong, ditampilkan per halaman (PAGE_SIZE).
     */
    private void displayData() {
        DefaultTableModel tableModel = new DefaultTableModel(TABLE_COLUMNS, 0);
        String keyword = jTextFieldPencarian.getText().trim().toLowerCase();
        boolean isSearching = !keyword.isEmpty() && !keyword.equals("pencarian");
 
        if (isSearching) {
            populateSearchResults(tableModel, keyword);
            jLabel2.setText("Hasil Pencarian");
        } else {
            populatePage(tableModel);
            int totalPages = calculateTotalPages(allData.size());
            jLabel2.setText("Halaman " + currentPage + " dari " + totalPages);
        }
 
        jTableDataMK.setModel(tableModel);
    }

    private void populateSearchResults(DefaultTableModel tableModel, String keyword) {
        for (Course course : allData) {
            boolean matchNama = course.getCourseName().toLowerCase().contains(keyword);
            boolean matchKode = course.getCode().toLowerCase().contains(keyword);
            if (matchNama || matchKode) {
                tableModel.addRow(buildRowData(course));
            }
        }
    }
 
    private void populatePage(DefaultTableModel tableModel) {
        int start = (currentPage - 1) * PAGE_SIZE;
        int end   = Math.min(start + PAGE_SIZE, allData.size());
        for (int i = start; i < end; i++) {
            tableModel.addRow(buildRowData(allData.get(i)));
        }
    }

    private Object[] buildRowData(Course course) {
        return new Object[]{
            course.getCode(),
            course.getCourseName(),
            course.getSKS(),
            course.getSemester()
        };
    }
 
    private int calculateTotalPages(int dataSize) {
        return (dataSize == 0) ? 1 : (int) Math.ceil((double) dataSize / PAGE_SIZE);
    }
    
