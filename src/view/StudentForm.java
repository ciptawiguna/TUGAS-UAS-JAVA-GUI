    private void jButtonSimpanActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonSimpanActionPerformed
        // TODO add your handling code here:
        String name        = jTextFieldNama.getText().trim();
        String cardID      = jTextFieldNIK.getText().trim();
        String nim         = jTextFieldNIM.getText().trim();
        String studyProgram = jComboBoxProdi.getSelectedItem().toString();
 
        if (name.isEmpty() || cardID.isEmpty() || nim.isEmpty()) {
            showError("Nama, NIK, dan NIM tidak boleh kosong!");
            return;
        }
 
        try {
            Student student = new Student(cardID, name, nim, studyProgram);
            studentController.saveOrUpdate(student, selectedNIM);
            showInfo("Data Mahasiswa berhasil disimpan!");
            clearForm();
            loadStudentData();
        } catch (IllegalArgumentException ex) {
            showError(ex.getMessage());
        } catch (SQLException ex) {
            showError("Gagal menyimpan: " + ex.getMessage());
        }
    }//GEN-LAST:event_jButtonSimpanActionPerformed

    private void jButtonDeleteActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeleteActionPerformed
        // TODO add your handling code here:
     if (selectedNIM == null || selectedNIM.isEmpty()) {
            JOptionPane.showMessageDialog(
                this, "Silakan pilih data mahasiswa di tabel terlebih dahulu!",
                "Peringatan", JOptionPane.WARNING_MESSAGE
            );
            return;
        }
        int confirm = JOptionPane.showConfirmDialog(
            this, "Apakah Anda yakin ingin menghapus mahasiswa dengan NIM: " + selectedNIM + "?",
            "Konfirmasi Hapus", JOptionPane.YES_NO_OPTION
        );
        if (confirm == JOptionPane.YES_OPTION) {
            try {
                studentController.delete(selectedNIM);
                showInfo("Mahasiswa berhasil dihapus!");
                clearForm();
                loadStudentData();
            } catch (SQLException ex) {
                Logger.getLogger(StudentForm.class.getName()).log(Level.SEVERE, null, ex);
                showError("Gagal menghapus: " + ex.getMessage());
            }
        }

    }//GEN-LAST:event_jButtonDeleteActionPerformed

