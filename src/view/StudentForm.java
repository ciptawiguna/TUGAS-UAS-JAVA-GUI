    private void loadStudentData() throws SQLException {
        allData     = studentController.getAll();
        currentPage = 1;
        displayData();
    }

    private void displayData() {
        String[] headers = TABLE_COLUMNS;
        javax.swing.table.DefaultTableModel tableModel =
            new javax.swing.table.DefaultTableModel(headers, 0);
 
        String keyword    = jTextFieldPencarian.getText().trim();
        List<Student> activeData = resolveActiveData(keyword);
 
        int start = (currentPage - 1) * PAGE_SIZE;
        int end   = Math.min(start + PAGE_SIZE, activeData.size());
 
        for (int i = start; i < end; i++) {
            tableModel.addRow(buildRowData(activeData.get(i)));
        }
 
        jTableStudent.setModel(tableModel);
 
        int totalPages = calculateTotalPages(activeData.size());
        jLabel2.setText("Halaman " + currentPage + " dari " + totalPages);
    }
 
    private List<Student> resolveActiveData(String keyword) {
        if (keyword.isEmpty() || keyword.equalsIgnoreCase("Pencarian")) {
            return allData;
        }
        List<Student> filtered = new java.util.ArrayList<>();
        String lowerKeyword = keyword.toLowerCase();
        for (Student student : allData) {
            boolean matchNama = student.getName().toLowerCase().contains(lowerKeyword);
            boolean matchNim  = student.getNim().toLowerCase().contains(lowerKeyword);
            if (matchNama || matchNim) {
                filtered.add(student);
            }
        }
        return filtered;
    }
 
    private Object[] buildRowData(Student student) {
        return new Object[]{
            student.getCardID(),
            student.getName(),
            student.getNim(),
            student.getStudyProgram()
        };
    }
 
    private int calculateTotalPages(int dataSize) {
        return (dataSize == 0) ? 1 : (int) Math.ceil((double) dataSize / PAGE_SIZE);
    }
 
